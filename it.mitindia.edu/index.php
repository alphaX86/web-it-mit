                            <p align="justify" style="color:#212121; font-size:12pt;">Keeping with the tradition of Anna University in offering new areas of specialization in engineering, the Department of Information Technology was instituted in the year 2001 with the objective of imparting knowledge in cutting-edge technologies in Information Technology and Computer Sciences.</p>
                            <p align="justify" style="color:#212121; font-size:12pt;">The department is currently headed by Dr. Dhananjay Kumar, an educator and researcher himself. In the footsteps of the former Heads, the incumbent Head is further taking the department to reach newer heights. The department has a team of dynamic, young and dedicated staff, with specialization in wide array of areas. It offers full time and part time courses for both UG and PG. The department receives funds from DST under FIST programme.</p>
                            <p align="justify" style="color:#212121;font-size:12pt;">All the members of TEAM IT share the belief that IT is a tool, if used effectively can improve the quality of all activities being carried out. As a first step, a project to automate all administrative activities has been taken up and the entire project is being done using the services of our department staffs. In the near future the department will have post Graduate course (other than MS By Research) offering curriculum in the field of Computer Science and Information Technology.</p>
                            <p align="justify" style="color:#212121; font-size:12pt;">Department of Information Technology, MIT, thrives to accomplish its vision of developing dexterous and proficient students. Its progress is growing exponentially in contributing efficient and technically sound students to the corporate world.</p>
